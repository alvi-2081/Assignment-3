void main(){
  var firstnum = 10;
  var secondnum = 5;
  var operation= "/";
  
  if (operation=="+"){
    print (firstnum + secondnum);
  }
  else if (operation=="-"){
    print (firstnum - secondnum); 
  }
  else if (operation=="*"){
    print (firstnum * secondnum);
  }
  else if (operation =="/"){
    print (firstnum / secondnum);
  }
  else if (operation == "%"){
    print (firstnum % secondnum);
  }
}